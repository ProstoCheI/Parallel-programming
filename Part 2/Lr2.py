def estrin_method(coeffs, x):
    """
    Вычисляет значение многочлена по схеме Эстрина.

    :param coeffs: Список коэффициентов [a_0, a_1, ..., a_n]
                   (ВАЖНО: здесь порядок от младшей степени к старшей!)
    :param x: Точка, в которой вычисляем значение
    """
    n = len(coeffs)

    # Базовый случай: если остался один коэффициент, это и есть значение
    if n == 1:
        return coeffs[0]

    # Вычисляем x в нужной степени для этого уровня "дерева"
    # На первом уровне это x, на втором x^2, на третьем x^4 и т.д.
    # Но для простоты реализации мы будем передавать x дальше.

    new_coeffs = []
    x_squared = x * x

    # Группируем коэффициенты парами: (a_i + a_{i+1}*x)
    for i in range(0, n - 1, 2):
        new_coeffs.append(coeffs[i] + coeffs[i + 1] * x)

    # Если количество коэффициентов было нечетным, добавляем последний без изменений
    if n % 2 != 0:
        new_coeffs.append(coeffs[-1])

    # Рекурсивно вызываем функцию для нового списка коэффициентов,
    # теперь "шаг" по x увеличивается (x становится x^2)
    return estrin_method(new_coeffs, x_squared)


# Пример: 2x^3 - 6x^2 + 2x - 1
# В формате [a_0, a_1, a_2, a_3] это будет: [-1, 2, -6, 2]
poly_coeffs = [-1, 2, -6, 2]
target_x = 3

result = estrin_method(poly_coeffs, target_x)

print(f"Результат вычисления по Эстрину: {result}")