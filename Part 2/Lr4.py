def solve_bidiagonal_parallel(d, u, b):
    """
    Параллельный алгоритм (рекурсивное удвоение) для верхней двухдиагональной системы.

    :param d: Главная диагональ [d_0, ..., d_{n-1}]
    :param u: Верхняя диагональ [u_0, ..., u_{n-2}]
    :param b: Свободные члены [b_0, ..., b_{n-1}]
    """
    n = len(d)

    # 1. Подготовка: приводим к виду x_i = c_i * x_{i+1} + g_i
    # Для последнего элемента x_{n-1} = b_{n-1}/d_{n-1}, c_{n-1} = 0
    c = [0.0] * n
    g = [0.0] * n

    for i in range(n - 1):
        c[i] = -u[i] / d[i]
        g[i] = b[i] / d[i]
    g[n - 1] = b[n - 1] / d[n - 1]
    c[n - 1] = 0  # Последнему элементу не на что ссылаться

    # 2. Основной цикл рекурсивного удвоения (логарифмический)
    # На каждом шаге 'step' увеличивается вдвое: 1, 2, 4, 8...
    step = 1
    while step < n:
        # В реальности этот цикл 'for' выполнялся бы параллельно на разных ядрах/потоках
        new_c = list(c)
        new_g = list(g)

        for i in range(n - step):
            # Формула: x_i = (c_i * c_{i+step}) * x_{i+2*step} + (c_i * g_{i+step} + g_i)
            new_c[i] = c[i] * c[i + step]
            new_g[i] = c[i] * g[i + step] + g[i]

        c = new_c
        g = new_g
        step *= 2

    # В конце работы алгоритма все c_i станут равны 0 (так как они "вылетят" за границу n),
    # а в массиве g останутся значения x_i.
    return g


# --- ПРИМЕР ---
# Та же система, что и в прошлый раз:
# 2x0 + 1x1 = 5
# 3x1 + 4x2 = 18
# 5x2       = 15

diag = [2.0, 3.0, 5.0]
upper = [1.0, 4.0]
results = [5.0, 18.0, 15.0]

solution = solve_bidiagonal_parallel(diag, upper, results)

print("Решение системы (параллельный метод):")
for i, val in enumerate(solution):
    print(f"x{i} = {val:.2f}")