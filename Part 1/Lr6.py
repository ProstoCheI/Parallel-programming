def multiply_matrices(A, B):
    """
    Перемножает две квадратные матрицы A и B одинакового размера.
    """
    n = len(A)
    # Создаем результирующую матрицу n x n, заполненную нулями
    C = [[0.0 for _ in range(n)] for _ in range(n)]

    # Тройной вложенный цикл
    for i in range(n):  # Проход по строкам матрицы A
        for j in range(n):  # Проход по столбцам матрицы B
            for k in range(n):  # Суммирование произведений элементов
                C[i][j] += A[i][k] * B[k][j]

    return C


def print_matrix(matrix, label):
    print(f"\n{label}:")
    for row in matrix:
        print([round(x, 2) for x in row])


# --- ПРИМЕР С ТВОИМИ ДАННЫМИ ---
# Возьмем матрицу A (двухдиагональную из прошлого примера)
# и умножим её на её же обратную матрицу B, чтобы проверить результат.

A = [
    [2.0, 0.0, 0.0],
    [1.0, 3.0, 0.0],
    [0.0, 2.0, 4.0]
]

# Обратная матрица (примерные значения из расчетов выше)
B = [
    [0.5, 0.0, 0.0],
    [1.0, 0.5, 0.0],
    [1.0, -0.5, 0.0]
]

result = multiply_matrices(A, B)

print_matrix(A, "Матрица A")
print_matrix(B, "Матрица B (обратная)")
print_matrix(result, "Результат A * B (Единичная матрица)")